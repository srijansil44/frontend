    {
        //
         $request->validate([
             'about_us' => 'required',
                 'terms_conditions' =>  'required'
    ]
         );

         $about_us = new About();

         $about_us->updateorcreate(['id'=>1],[
             'about_us' => $request->about_us,
             'terms_conditions' => $request->terms_conditions

         ]);

         return redirect()->back();

